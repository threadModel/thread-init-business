const renderServiceDetail = (queryPrompt) => {
    const service = ServiceData.find((p) => p.name === queryPrompt);
    const serviceContent = document.getElementById("serviceContent");
    const servicesList = document.getElementById("services-list");
    const pdf = document.getElementById("pdf");
    const doc = document.getElementById("doc");

    if (!service) {
        serviceContent.innerHTML = "<p>Service not found.</p>";
        return;
    }
    pdf.setAttribute("href", service.pdf);
    doc.setAttribute("href", service.doc);
    const links = servicesList.querySelectorAll("a");
    links.forEach((link) => {
        const hrefText = link.getAttribute("href").split("?")[1]; // Get the text after '?'
        if (hrefText === queryPrompt) {
            link.classList.add("active");
        } else {
            link.classList.remove("active");
        }
    });

    const renderedContent = service.content.replace("{BANNER}", service.banner);
    serviceContent.innerHTML = renderedContent;

};

const fullUrl = window.location.href;
const queryString = fullUrl.split('?')[1];
renderServiceDetail(queryString);
